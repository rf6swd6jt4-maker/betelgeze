import { readdirSync, readFileSync, writeFileSync } from "node:fs"
import { basename } from "node:path"

const [ssaDir, censusCsv, outputPath] = process.argv.slice(2)

if (!ssaDir || !censusCsv || !outputPath) {
    console.error("Usage: node scripts/generate-name-frequency-data.mjs <ssa-yob-dir> <census-surname-csv> <output-js>")
    process.exit(1)
}

const MIN_GIVEN_TOTAL = 10

function key(value) {
    return value.toLowerCase().replace(/[^a-z]/g, "")
}

function countPairs(map, minCount = 0) {
    return [...map.entries()]
        .filter(([name, count]) => name && count >= minCount)
        .sort(([left], [right]) => left.localeCompare(right))
        .map(([name, count]) => `${name}:${count}`)
}

function chunks(values, size = 220) {
    const output = []
    for (let index = 0; index < values.length; index += size) output.push(values.slice(index, index + size))
    return output
}

const givenCounts = new Map()
let totalGivenCount = 0
for (const fileName of readdirSync(ssaDir).filter((name) => /^yob\d+\.txt$/.test(name)).sort()) {
    const text = readFileSync(`${ssaDir}/${fileName}`, "utf8")
    for (const line of text.split(/\r?\n/)) {
        if (!line.trim()) continue
        const [name, _sex, countText] = line.split(",")
        const count = Number(countText)
        const nameKey = key(name)
        if (!nameKey || !Number.isFinite(count)) continue
        givenCounts.set(nameKey, (givenCounts.get(nameKey) ?? 0) + count)
        totalGivenCount += count
    }
}

const surnameCounts = new Map()
let totalSurnameCount = 0
for (const [index, line] of readFileSync(censusCsv, "utf8").split(/\r?\n/).entries()) {
    if (index === 0 || !line.trim()) continue
    const [name, _rank, countText] = line.split(",")
    const count = Number(countText)
    const nameKey = key(name)
    if (!nameKey || !Number.isFinite(count)) continue
    surnameCounts.set(nameKey, count)
    totalSurnameCount += count
}

const givenPairs = countPairs(givenCounts, MIN_GIVEN_TOTAL)
const surnamePairs = countPairs(surnameCounts)

function dataArray(name, values) {
    const lines = chunks(values).map((chunk) => `    "${chunk.join("|")}",`)
    return `export const ${name} = [\n${lines.join("\n")}\n].join("|")\n`
}

const output = `// Generated by ${basename(process.argv[1])}.
// Sources:
// - SSA national baby names ZIP, https://www.ssa.gov/oact/babynames/limits.html
// - Census 2010 surnames ZIP, https://www2.census.gov/topics/genealogy/2010surnames/
// Given names are included when the all-year national total is at least ${MIN_GIVEN_TOTAL}.

export const NAME_FREQUENCY_SOURCE = {
    givenNames: "Social Security Administration national baby names, all years in names.zip",
    surnames: "U.S. Census Bureau 2010 surnames, Names_2010Census.csv",
    minGivenTotal: ${MIN_GIVEN_TOTAL},
    givenNameCount: ${givenPairs.length},
    surnameCount: ${surnamePairs.length},
    totalGivenObservations: ${totalGivenCount},
    totalSurnameObservations: ${totalSurnameCount},
}

${dataArray("GIVEN_NAME_COUNT_DATA", givenPairs)}
${dataArray("SURNAME_COUNT_DATA", surnamePairs)}
`

writeFileSync(outputPath, output)
console.log(JSON.stringify({
    outputPath,
    minGivenTotal: MIN_GIVEN_TOTAL,
    givenNameCount: givenPairs.length,
    surnameCount: surnamePairs.length,
    totalGivenObservations: totalGivenCount,
    totalSurnameObservations: totalSurnameCount,
}, null, 2))
